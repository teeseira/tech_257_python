x = 3.0
y = 5.0

print("\nArithmetic operators:")

result = x + y + 2
print("Let's show x + y + 2 =", result, "and the type is:", type(result))
result = x - y - 2
print("Let's show x - y - 2 =", result, "and the type is:", type(result))
result = x * y * 2
print("Let's show x * y * 2 =", result, "and the type is:", type(result))
result = x / y / 2
print("Let's show x / y / 2 =", result, "and the type is:", type(result))
result = x % y % 2 # gives the remainder
print("Let's show x % y =", result, "and the type is:", type(result))

print("\nComparison operators:")
result = x > y
print("Let's show x > y =", result, "and the type is:", type(result))
result = x < y
print("Let's show x < y =", result, "and the type is:", type(result))
result = x >= y
print("Let's show x >= y =", result, "and the type is:", type(result))
result = x <= y
print("Let's show x <= y =", result, "and the type is:", type(result))
result = x == y
print("Let's show x == y =", result, "and the type is:", type(result))
result = x != y
print("Let's show x != y =", result, "and the type is:", type(result))